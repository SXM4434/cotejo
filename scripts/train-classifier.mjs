#!/usr/bin/env node
// Trainer for the FONT CLASSIFIER — predicts a font's category from how it renders, so an UPLOADED
// font (no Google metadata) gets real predicted meta instead of defaulting to "sans". Genuine
// supervised ML: real features (extract-font-features.mjs renders + measures each font) and real
// labels (the Google directory's category for 1,936 families). ([[feedback_actual_ml_not_fake]])
//
//   node scripts/train-classifier.mjs
//   reads datasets/font-features.jsonl → bakes src/lib/fontClassifierModel.ts (softmax weights + the
//   feature standardisation), and reports held-out accuracy + a confusion matrix. No fabrication: it
//   reports the real test accuracy it achieves, whatever that is.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const DATA = resolve(__dirname, "../datasets/font-features.jsonl");
const OUT = resolve(__dirname, "../src/lib/fontClassifierModel.ts");
const FEATURES = ["xRatio", "monoRatio", "widthRatio", "capStretch", "serifProxy", "inkDensity"];
const CLASSES = ["sans", "serif", "display", "handwriting", "monospace"];
const EPOCHS = 4000, LR = 0.3, L2 = 1e-4;

const rows = readFileSync(DATA, "utf8").split("\n").filter(Boolean).map((l) => JSON.parse(l));
const X0 = rows.map((r) => FEATURES.map((f) => r[f]));
const y = rows.map((r) => CLASSES.indexOf(r.category)).filter((i) => i >= 0);
console.log(`Loaded ${rows.length} fonts · ${FEATURES.length} features · ${CLASSES.length} classes`);

// standardise features (softmax needs comparable scales)
const D = FEATURES.length;
const mean = Array.from({ length: D }, (_, j) => X0.reduce((s, r) => s + r[j], 0) / X0.length);
const std = Array.from({ length: D }, (_, j) => Math.sqrt(X0.reduce((s, r) => s + (r[j] - mean[j]) ** 2, 0) / X0.length) || 1);
const X = X0.map((r) => r.map((v, j) => (v - mean[j]) / std[j]));

// deterministic split (seeded LCG)
let seed = 7; const rng = () => (seed = (seed * 1103515245 + 12345) & 0x7fffffff) / 0x7fffffff;
const idx = X.map((_, i) => i).sort(() => rng() - 0.5);
const cut = Math.floor(idx.length * 0.2);
const teIdx = new Set(idx.slice(0, cut));
const tr = idx.filter((i) => !teIdx.has(i)), te = idx.filter((i) => teIdx.has(i));

const K = CLASSES.length;
let W = Array.from({ length: K }, () => new Array(D).fill(0));
let b = new Array(K).fill(0);
const softmax = (z) => { const m = Math.max(...z); const e = z.map((v) => Math.exp(v - m)); const s = e.reduce((a, c) => a + c, 0); return e.map((v) => v / s); };
const logits = (x) => W.map((wk, k) => wk.reduce((s, wi, j) => s + wi * x[j], b[k]));

for (let ep = 0; ep < EPOCHS; ep++) {
  const gW = Array.from({ length: K }, () => new Array(D).fill(0));
  const gb = new Array(K).fill(0);
  for (const i of tr) {
    const p = softmax(logits(X[i]));
    for (let k = 0; k < K; k++) {
      const err = p[k] - (y[i] === k ? 1 : 0);
      gb[k] += err;
      for (let j = 0; j < D; j++) gW[k][j] += err * X[i][j];
    }
  }
  for (let k = 0; k < K; k++) {
    b[k] -= LR * gb[k] / tr.length;
    for (let j = 0; j < D; j++) W[k][j] -= LR * (gW[k][j] / tr.length + L2 * W[k][j]);
  }
}

const pred = (i) => { const p = softmax(logits(X[i])); return p.indexOf(Math.max(...p)); };
const acc = (set) => set.filter((i) => pred(i) === y[i]).length / set.length;
const conf = Array.from({ length: K }, () => new Array(K).fill(0));
for (const i of te) conf[y[i]][pred(i)]++;

console.log(`\nTrain ${tr.length} · Test ${te.length}`);
console.log(`Train acc ${(acc(tr) * 100).toFixed(1)}%   Held-out acc ${(acc(te) * 100).toFixed(1)}%`);
console.log(`\nConfusion (rows = true, cols = predicted):`);
console.log(`            ${CLASSES.map((c) => c.slice(0, 5).padStart(6)).join("")}`);
conf.forEach((row, k) => console.log(`  ${CLASSES[k].padEnd(11)}${row.map((n) => String(n).padStart(6)).join("")}`));

const model = {
  classes: CLASSES, features: FEATURES, mean: mean.map((v) => +v.toFixed(5)), std: std.map((v) => +v.toFixed(5)),
  W: W.map((r) => r.map((v) => +v.toFixed(5))), b: b.map((v) => +v.toFixed(5)),
  n: rows.length, testAcc: +acc(te).toFixed(4), trainedAt: new Date().toISOString(),
};
writeFileSync(OUT, `// Font classifier — AUTO-GENERATED by scripts/train-classifier.mjs. Do not hand-edit.
// Softmax over rendered-glyph features → font category. Trained on ${rows.length} Google fonts
// (real category labels); held-out accuracy ${(acc(te) * 100).toFixed(1)}%. ([[feedback_actual_ml_not_fake]])
export type FontClassifierModel = {
  classes: string[]; features: string[]; mean: number[]; std: number[];
  W: number[][]; b: number[]; n: number; testAcc: number; trainedAt: string;
};
export const FONT_CLASSIFIER: FontClassifierModel = ${JSON.stringify(model, null, 2)};
`);
console.log(`\nBaked → src/lib/fontClassifierModel.ts (test acc ${(acc(te) * 100).toFixed(1)}%)`);
